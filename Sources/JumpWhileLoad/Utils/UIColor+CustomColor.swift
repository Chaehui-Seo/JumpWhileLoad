import UIKit

extension UIColor {
    internal static var customDarkGray : UIColor {
        return UIColor(red: 83/255.0, green: 83/255.0, blue: 83/255.0, alpha: 1.0)
    }
    
    internal static var customLightGray : UIColor {
        return UIColor(red: 217/255.0, green: 217/255.0, blue: 217/255.0, alpha: 1.0)
    }
}
